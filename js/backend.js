    let judul = document.forms['form-tambah-gosip']['txt-judul'].value
    judul = judul.split(" ")
    let deskripsi = document.forms['form-tambah-gosip']['txt-desc'].value
    let gambar = document.forms['form-tambah-gosip']['txt-link-img'].value
    if (nama === "") {
        return alert(`Masukkan nama Anda!`);
    }
    if (judul === "") {
        return alert(`Masukkan judul berita!`);
    }
    if (deskripsi === "") {
        return alert(`Masukkan deskripsi berita!`);
    }
    let temp = ""
    for (let i = 0; i < 8; i++) {
        temp += gambar[i]
    }
    if (temp !== "https://") {
        return alert(`Masukkan link gambar dengan format https://`)
    }
    if (gambar === "") {
        return alert(`Masukkan link gambar!`);
    }
    if (deskripsi.length < 20) {
        return alert(`deskripsi berita harus detail`)
    }

    let max = Number.MIN_VALUE

    for (let i = 0; i < newsList.length; i++) {
        if (newsList[i]['id'] > max) {
            max = newsList[i]['id']
        }
    }

    let obj = {}
    obj["id"] = max + 1
    obj["author"] = nama
    obj["tanggal"] = new Date()
    obj["judul"] = judul
    obj["content"] = deskripsi
    obj["img"] = gambar

    // newsList.push(obj)
    newsList.unshift(obj)
