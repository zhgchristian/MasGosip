function generateNews(array) {
    for (let i = 0; i < array.length; i++) {
        const content = document.querySelector('#body-article')
        const div = document.createElement('div')
        div.classList.add("main-content-card-article")
        // div.setAttribute("id", i + 1000)

        const img = document.createElement('img')
        img.src = array[i].img
        div.append(img)

        const title = document.createElement('h4')
        let judul = array[i].judul.join(" ")
        judul = judul.toUpperCase()
        title.innerText = judul
        div.append(title)

        const cerita = document.createElement('p')
        let isi = array[i].content
        cerita.innerText = isi
        div.append(cerita)

        const anchor = document.createElement('a')
        anchor.href = "#"
        anchor.innerText = 'Baca selengkapnya'

        let anchorId = array[i]['id']   //refer ke id news di array newsList

        anchor.setAttribute("id", anchorId)
        anchor.addEventListener("click", () => generateDetailPage(anchorId))
        div.append(anchor)
        content.append(div)
    }
}
